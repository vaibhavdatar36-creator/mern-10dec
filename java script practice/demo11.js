function add(n1, n2) {
    return n1 + n2
}

function sub(n1, n2) {
    return n1 - n2
}

function mul(n1, n2) {
    return n1 * n2
}

function div(n1, n2) {
    return n1 / n2
}

const res1 = add(20, 10)
console.log(`Result - ${res1}`)

const res2 = sub(20, 10)
console.log(`Result - ${res2}`)

const res3 = mul(20, 10)
console.log(`Result - ${res3}`)

const res4 = div(20, 10)
console.log(`Result - ${res4}`)
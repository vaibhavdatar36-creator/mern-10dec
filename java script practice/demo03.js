// datatypes in JS
let n1;

console.log(`n1 - ${n1}, typeof(n1) - ${typeof (n1)}`)
// value = sunbeam , datatype - string

n1 = 10
console.log(`n1 - ${n1}, typeof(n1) - ${typeof (n1)}`) // template String
// value = 10 , datatype - number
// console.log('n1 - ' + n1 + ', typeof(n1) - ' + typeof (n1))

n1 = 10.22
console.log(`n1 - ${n1}, typeof(n1) - ${typeof (n1)}`)
// value = 10.22 , datatype - number

n1 = true
console.log(`n1 - ${n1}, typeof(n1) - ${typeof (n1)}`)
// value = true , datatype - boolean

n1 = 'sunbeam'
console.log(`n1 - ${n1}, typeof(n1) - ${typeof (n1)}`)
// value = sunbeam , datatype - string

n1 = null
console.log(`n1 - ${n1}, typeof(n1) - ${typeof (n1)}`)
// value = null , datatype - object